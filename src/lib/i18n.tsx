const I18nCtx = createContext<I18nContextType | null>(null);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem('sahti_appLang') as Lang;
    const l = saved || 'ar';
    document.documentElement.dir = l === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = l;
    return l;
  });
  const [labLang, setLabLangState] = useState<Lang>(() => {
    return (localStorage.getItem('sahti_labLang') as Lang) || 'ar';
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem('sahti_appLang', l);
  };

  const setLabLang = (l: Lang) => {
    setLabLangState(l);
    localStorage.setItem('sahti_labLang', l);
  };

  useEffect(() => {
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }, [lang]);

  const t = (key: string): string => {
    return translations[lang][key] || translations['ar'][key] || key;
  };

  const tLabName = (key: string, arabicName: string): string => {
    if (labLang === 'en' && LAB_TEST_EN[key]) return LAB_TEST_EN[key];
    return arabicName;
  };

  return (
    <I18nCtx.Provider value={{ lang, labLang, setLang, setLabLang, t, isRTL: lang === 'ar', tLabName }}>
      {children}
    </I18nCtx.Provider>
  );
};

export const useLanguage = () => {
  const ctx = useContext(I18nCtx);
  if (!ctx) throw new Error('useLanguage must be within LanguageProvider');
  return ctx;
};
